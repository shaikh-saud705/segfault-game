# normalise fancy unicode punctuation down to glyphs we have (also helps with
# LLM-generated taunt text)
_NORM = str.maketrans({
    "—": "-", "–": "-",            # em / en dash
    "’": "'", "‘": "'",            # curly single quotes
    "“": '"', "”": '"',            # curly double quotes
    "…": "...",                          # ellipsis
})

# sanity: every glyph row must be exactly GW wide
for _name, _rows in GLYPHS.items():
    assert len(_rows) == GH and all(len(r) == GW for r in _rows), \
        f"bad pixelfont glyph: {_name!r}"

_cache = {}


def render(text, scale=8, color=WHITE, tracking=1):
    """Render `text` as a crisp pixel surface. `tracking` = blank cols between
    glyphs (in source pixels). Cached."""
    key = (text, scale, color, tracking)
    if key in _cache:
        return _cache[key]

    text = text.translate(_NORM).upper()
    glyphs = [GLYPHS.get(ch, GLYPHS[" "]) for ch in text]
    cols = len(glyphs) * (GW + tracking) - tracking if glyphs else 0
    base = pygame.Surface((max(1, cols), GH), pygame.SRCALPHA)
    cx = 0
    for g in glyphs:
        for y, row in enumerate(g):
            for x, ch in enumerate(row):
                if ch == "#":
                    base.set_at((cx + x, y), color)
        cx += GW + tracking

    out = pygame.transform.scale(base, (base.get_width() * scale,
                                        base.get_height() * scale))
    _cache[key] = out
    return out


def width(text, scale=8, tracking=1):
    n = len(text)
    return (n * (GW + tracking) - tracking) * scale if n else 0


def height(scale=8):
    return GH * scale


def draw(surface, text, scale, x, y, color=WHITE, center=False, shadow=True):
    """Blit pixel text. Returns the blitted rect."""
    surf = render(text, scale, color)
    rect = surf.get_rect()
    if center:
        rect.center = (x, y)
    else:
        rect.topleft = (x, y)
    if shadow:
        sh = render(text, scale, (0, 0, 0))
        surface.blit(sh, rect.move(scale, scale))
    surface.blit(surf, rect)
    return rect
